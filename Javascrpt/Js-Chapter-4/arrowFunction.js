// Compact way of writing a function

// sum function 
function sum(a, b){
    return a + b;
}
// arrow function
const arrowSum = (a, b) => {
    return a  + b;
};
console.log(arrowSum(3,4));

// multiplication function
function mul(a, b) {
    return a * b;
}

// arrow function of multiplication
const arrowMul = (a, b) => {
    return a * b;
}
console.log(arrowMul(3, 4));